    private void BtnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCadastrarActionPerformed
        String cliente = CbxCliente.getSelectedItem().toString();
        String produto = CbxProduto.getSelectedItem().toString();
        String situacao = CbxSituacao.getSelectedItem().toString();
        int quantidade = Integer.parseInt(CbxQuantidade.getSelectedItem().toString());
        int idCliente = Integer.parseInt(cliente.split(" - ")[0]);
        int idProduto = Integer.parseInt(produto.split(" - ")[0]);
        
        Conexao conexao = new Conexao();
      if (conexao.conectar()) {
    try {
       
        String sqlEstoque = "SELECT estoque FROM produtos WHERE id = ?";
        PreparedStatement psEstoque = conexao.getConnection().prepareStatement(sqlEstoque);
        psEstoque.setInt(1, idProduto);
        ResultSet rs = psEstoque.executeQuery();

        if (rs.next()) {
            int estoque = Integer.parseInt(rs.getString("estoque"));

            if (quantidade > estoque) {
                JOptionPane.showMessageDialog(this, "Estoque insuficiente!");
                return;
            }

          
            String sql = "INSERT INTO pedidos (situacao, fk_cliente_id, FK_produto_id) VALUES (?, ?, ?)";
            PreparedStatement ps = conexao.getConnection().prepareStatement(sql);
            ps.setString(1, situacao);
            ps.setInt(2, idCliente);
            ps.setInt(3, idProduto);
            ps.executeUpdate();

          
            int novoEstoque = estoque - quantidade;
            String sqlUpdate = "UPDATE produtos SET estoque = ? WHERE id = ?";
            PreparedStatement psUpdate = conexao.getConnection().prepareStatement(sqlUpdate);
            psUpdate.setString(1, String.valueOf(novoEstoque)); // estoque ainda √© VARCHAR
            psUpdate.setInt(2, idProduto);
            psUpdate.executeUpdate();

            JOptionPane.showMessageDialog(this, "Pedido cadastrado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Produto n√£o encontrado.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }
}
       this.dispose();
    }//GEN-LAST:event_BtnCadastrarActionPerformed

