    private void BtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnConsultarActionPerformed
String categoriaBusca = TxtConsultar.getText();
try {
    Conexao conexao = new Conexao();
    if (conexao.conectar()) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Situacao");
        modelo.addColumn("Cliente");
        modelo.addColumn("Produto");

        String sql = "SELECT p.id, p.situacao, c.nome AS cliente_nome, pr.nome AS produto_nome " +
                     "FROM pedidos p " +
                     "JOIN clientes c ON p.fk_cliente_id = c.id " +
                     "JOIN produtos pr ON p.fk_produto_id = pr.id " +
                     "WHERE c.nome LIKE ?";

        PreparedStatement stmt = conexao.getConnection().prepareStatement(sql);
        stmt.setString(1, "%" + categoriaBusca + "%");

        ResultSet rs = stmt.executeQuery();

        while (rs.next()) {
            modelo.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("situacao"),
                rs.getString("cliente_nome"),
                rs.getString("produto_nome")
            });
        }

        jTable2.setModel(modelo);
    }
} catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Erro ao consultar: " + e.getMessage());
}
    }//GEN-LAST:event_BtnConsultarActionPerformed

    private void BtnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAtualizarActionPerformed
       int linha = jTable2.getSelectedRow();
    if (linha == -1) {
        JOptionPane.showMessageDialog(this, "Selecione uma linha para atualizar.");
        return;
    }

    if (jTable2.isEditing()) {
        jTable2.getCellEditor().stopCellEditing();
    }

    try {
        int id = Integer.parseInt(jTable2.getValueAt(linha, 0).toString());
        String situacao = jTable2.getValueAt(linha, 1).toString();
        String nomeCliente = jTable2.getValueAt(linha, 2).toString();
        String nomeProduto = jTable2.getValueAt(linha, 3).toString();

        Conexao conexao = new Conexao();
        if (conexao.conectar()) {

            
            int clienteId = -1;
            String sqlBuscaCliente = "SELECT id FROM clientes WHERE nome = ?";
            PreparedStatement stmtCliente = conexao.getConnection().prepareStatement(sqlBuscaCliente);
            stmtCliente.setString(1, nomeCliente);
            ResultSet rsCliente = stmtCliente.executeQuery();
            if (rsCliente.next()) {
                clienteId = rsCliente.getInt("id");
            } else {
                JOptionPane.showMessageDialog(this, "Cliente não encontrado.");
                return;
            }

          
            int produtoId = -1;
            String sqlBuscaProduto = "SELECT id FROM produtos WHERE nome = ?";
            PreparedStatement stmtProduto = conexao.getConnection().prepareStatement(sqlBuscaProduto);
            stmtProduto.setString(1, nomeProduto);
            ResultSet rsProduto = stmtProduto.executeQuery();
            if (rsProduto.next()) {
                produtoId = rsProduto.getInt("id");
            } else {
                JOptionPane.showMessageDialog(this, "Produto não encontrado.");
                return;
            }

           
            String sql = "UPDATE pedidos SET situacao = ?, fk_cliente_id = ?, FK_produto_id = ? WHERE id = ?";
            PreparedStatement stmt = conexao.getConnection().prepareStatement(sql);
            stmt.setString(1, situacao);
            stmt.setInt(2, clienteId);
            stmt.setInt(3, produtoId);
            stmt.setInt(4, id);

            int resultado = stmt.executeUpdate();

            if (resultado > 0) {
                JOptionPane.showMessageDialog(this, "Pedido atualizado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Nenhum registro foi atualizado.");
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }   

   
    }//GEN-LAST:event_BtnAtualizarActionPerformed

