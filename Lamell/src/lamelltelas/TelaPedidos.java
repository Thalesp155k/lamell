    private void BtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnExcluirActionPerformed
       int linha = jTable2.getSelectedRow();
if (linha == -1) {
    JOptionPane.showMessageDialog(this, "Selecione um pedido para excluir.");
    return;
}


int idSelecionado = Integer.parseInt(jTable2.getValueAt(linha, 0).toString());

int confirm = JOptionPane.showConfirmDialog(this, "Deseja excluir o pedido selecionado?");
if (confirm == JOptionPane.YES_OPTION) {
    try {
        Conexao conexao = new Conexao();
        if (conexao.conectar()) {
            String sql = "DELETE FROM pedidos WHERE id = ?";
            PreparedStatement stmt = conexao.getConnection().prepareStatement(sql);
            stmt.setInt(1, idSelecionado);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Pedido excluído com sucesso!");

            BtnConsultar.doClick(); 
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Erro ao excluir: " + e.getMessage());
    }
}
    }//GEN-LAST:event_BtnExcluirActionPerformed

